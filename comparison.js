// comparison operators
console.log(1 > 9)
console.log(1 >= 9)
console.log(1 < 9)
console.log(1 <= 9)
console.log(1 != 9)
console.log(2 == "2") //returns true because the string "2" is converted to the number 2 before comparison
console.log(2 === "2") // returns false because, even though the values are the same, the types (number and string) are different

